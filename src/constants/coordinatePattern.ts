/**
 * Coordinate regex patterns for DD, DMS, and DDM
 *
 * Developed by: UofG - SH21 Team
 * As part of COMPSCI 4047 Team Project (H) course at the University of Glasgow
 */

export const coordPattern = {
  long: {
    isDD: /^([-+]?((0[0-9]{2}|1[0-7][0-9]|[0-9]{1,2})(\.[0-9]{0,8})?|180(\.0{0,8})?))$/,
    isDMS:
      /^([-+]?((0[0-9]{2}|1[0-7][0-9]|[0-9]{1,2})|180) ([0-5][0-9]|[0-9]) (([0-5][0-9]|[0-9])(\.[0-9]{0,8})?(E|e|W|w)))$/,
    isDMM:
      /^([-+]?((0[0-9]{2}|1[0-7][0-9]|[0-9]{1,2})|180) (([0-5][0-9]|[0-9])(\.[0-9]{0,8})?(E|e|W|w)?))$/,
  },
  lat: {
    isDD: /^([-+]?(([0-8][0-9]|[0-9])(\.[0-9]{0,8})?|90(\.0{0,8})?))$/,
    isDMS:
      /^([-+]?(([0-8][0-9]|[0-9])|90) ([0-5][0-9]|[0-9]) (([0-5][0-9]|[0-9])(\.[0-9]{0,8})?(N|n|S|s)))$/,
    isDMM:
      /^([-+]?(([0-8][0-9]|[0-9])|90) (([0-5][0-9]|[0-9])(\.[0-9]{0,8})?(N|n|S|s)?))$/,
  },
};
